package java10;

/**
 * 
 * @author masahiro
 * 連番クラスIdを、以下のように変更したクラスExIdを作成せよ。
 * インスタンスを生成するたびに識別番号をnずつ増やして与える（nは正の整数）
 * nの数は、指定されない限り1とするが、メソッドを通じて変更可能
 */

/* ====================================================================== */
/**
 * @brief 
 * 連番クラス改
 *
 * @note
 * 
 */
/* ====================================================================== */

public class ExId {
	
	/* ====フィールド==== */
	static int counter = 0; //何番までの識別番号を与えたか
	private static int addedValue = 1; //識別番号加算値 (変更可能だが変更前は１)	
	private int id; //識別番号
	
	/* ====コンストラクタ==== */
	public ExId(){
		//何番までの識別番号を与えたかをカウント
		++counter;
		//インスタンス生成ごとに特定の識別番号を加算する。
		id = addedValue * counter;
	}
	
	/* ====================================================================== */
	/**
	 * @brief 
	 * 識別番号を取得するメソッド
	 *
	 * @param なし
	 *
	 * @return 
	 * 
	 *
	 * @note
	 */
	/* ====================================================================== */
	public int getId(){
		//識別番号を返却
		return id;
	}
	
	/* ====================================================================== */
	/**
	 * @brief 
	 * 最後に与えた識別番号を取得するメソッド
	 *
	 * @param なし
	 *
	 * @return 
	 * 
	 *
	 * @note
	 * インスタンスをn個生成した時点で呼び出すとnを返すこと
	 */
	/* ====================================================================== */
	public static int getMaxId(){
		//最後に与えた識別番号を返却
		return counter;
	}
	
	/* ====================================================================== */
	/**
	 * @brief 
	 * 加算値を変更するメソッド
	 *
	 * @param changeValue　変更する値
	 *
	 * @return 
	 * 
	 * @note
	 * 
	 */
	/* ====================================================================== */
	public static void setAddedValue(int changeValue){
		//加算値を更新
		addedValue = changeValue;
	}
}